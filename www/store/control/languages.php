/*
  ADDITIONAL LANGUAGES
  -----------------------------------------------------------------------------------------------------------------

  To add additional languages, do the following:

  1. Make a copy of the main content/language/english/ folder

     Examples: german,spanish

  2. Add languages to $systemLang array. Each entry is an array with 2 slots:

     0 => folder name
     1 => text display

     Example:

     $systemLang[] = array(
       'german',
       'German'
     );

     Note the [] brackets after $systemLang. Add as many as you need. Examples for German and Spanish:

     $systemLang[] = array(
       'german',
       'German'
     );
     $systemLang[] = array(
       'spanish',
       'Spanish'
     );

     Any languages added here will appear with the default language in the drop down menu.

     If you add additional languages, clear the cache so the new languages show.

-------------------------------------------------------------------------------------------------------------------*/

